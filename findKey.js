const findKey = function(obj, callBack) {
  let keys = Object.keys(obj);
  for (key of keys) {
    if (callBack(obj[key])) {
      // console.log(key);
      return key;
    }
  }
  return undefined;
};

findKey({
  "Blue Hill": { stars: 1 },
  "Akaleri":   { stars: 3 },
  "noma":      { stars: 2 },
  "elBulli":   { stars: 3 },
  "Ora":       { stars: 2 },
  "Akelarre":  { stars: 3 }
}, x => x.stars === 2); // => "noma"

